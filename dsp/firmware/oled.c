static const SPIConfig oled_spi_cfg = {
  .end_cb = NULL,
  .ssport = GPIOB,
  .sspad = GPIOB_LCD_CS,
  .cr1 = SPI_CR1_BR_2 | SPI_CR1_CPOL | SPI_CR1_CPHA
};

static void oled_start(void)
{
    spiStart(&OLED_SPID, &oled_spi_cfg);
}

static void oled_stop(void)
{
    spiStop(&OLED_SPID);
}

static const uint8_t oled_nibble_stretch[16] = {
    0x00, 0x03, 0x0c, 0x0f, 0x30, 0x33, 0x3c, 0x3f,
    0xc0, 0xc3, 0xcc, 0xcf, 0xf0, 0xf3, 0xfc, 0xff
};

void oled_init() {
    /* Reset OLED */
    palClearLine(LINE_LCD_RESET);
    palSetLine(LINE_LCD_CS);
    chThdSleepMilliseconds(1000);
    palSetLine(LINE_LCD_RESET);

    oled_start();

    /* Send initialisation commands */
    palClearLine(LINE_LCD_DC);
    palClearLine(LINE_LCD_CS);
    spiSend(&OLED_SPID, sizeof(oled_init_cmds), (void*)oled_init_cmds);
    palSetLine(LINE_LCD_CS);

    oled_stop();
}

